				<div id="description">
					<span>
						PassBad connaît une belle réussite depuis son lancement en 2002. Le principe de ce dispositif est simple : disposer d'un outil permettant de valoriser la progression des jeunes joueurs de clubs.<br /><br />
						PassBad comprend cinq plumes de couleurs différentes, blanche, jaune, verte, bleue et rouge, qui valident les étapes de progression du joueur. Les épreuves de PassBad abordent :<br />
						<ul>
							<li>le domaine technique : manipulation de la raquette, placement, déplacement</li>
							<li>le domaine tactique : trajectoires, prise en compte d'une opposition</li>
							<li>la connaissance de l'activité : respect des règles et connaissance de l'environnement badminton.</li>
						</ul>

						L'ensemble des plumes peut s'obtenir sur deux saisons au rythme d'une par semestre. Le joueur dispose d'un passeport où il colle les plumes obtenues. Lorsqu'il a acquis les cinq plumes, la Fédération lui décerne un diplôme et lui délivre un premier classement à partir de la catégorie benjamin. Le public concerné par PassBad est principalement constitué de poussins et benjamins, mais dans la pratique rien n'empêche de le faire passer aux plus jeunes ou aux plus âgés. Les points forts de PassBad sont multiples : faire prendre conscience aux joueurs de leur progression, valoriser et fidéliser les jeunes pratiquants de l’école de jeunes, disposer d'un outil d’évaluation des apprentissages.
					</span>
				</div>
				
